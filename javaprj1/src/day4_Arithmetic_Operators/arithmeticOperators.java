package day4_Arithmetic_Operators;

public class arithmeticOperators {

	public static void main(String[] args) {
		
		//Addition
		System.out.println(55+5);
		
		int chairs=140;
		System.out.println(chairs+3);
		
		int morechairs=150;
		System.out.println(morechairs+3);
		
		//Subtraction
		double revenue=1200.58;
		double expenses=688.90;
		
		System.out.println("COCO monthly revenue estimated to be "+ (revenue-expenses));
		
		//Multiplication
		System.out.println(12*21);
		System.out.println(1000*0.25);
		
		//Division
		System.out.println(123456/22);
//		System.out.println(11/0); //Java throws error
		
	}

}
