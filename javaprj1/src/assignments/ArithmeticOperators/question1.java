package assignments.ArithmeticOperators;

public class question1 {

	public static void main(String[] args) {
		/*
		Write a program to convert gallons into Liters and display it.

	        Sample output:
	
	       15 gallon is 56.7812 liter
		 */
		
		double gallons=15;
		double liters=gallons*3.78541;
		
		System.out.println(gallons + " gallon is " + liters + " liter.");
		
	}

}
